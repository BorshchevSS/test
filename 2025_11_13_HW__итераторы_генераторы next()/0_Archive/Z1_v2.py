def run_recipe_interactive(instructions):
    """
    –ü–æ–∑–≤–æ–ª—è–µ—Ç –ø–æ–≤–∞—Ä—É –ø–µ—Ä–µ–º–µ—â–∞—Ç—å—Å—è –ø–æ —à–∞–≥–∞–º —Ä–µ—Ü–µ–ø—Ç–∞ –≤–ø–µ—Ä–µ–¥ –∏ –Ω–∞–∑–∞–¥.
    """
    current_step = 0
    total_steps = len(instructions)

    print("–†–µ—Ü–µ–ø—Ç –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è! –ò—Å–ø–æ–ª—å–∑—É–π—Ç–µ –∫–æ–º–∞–Ω–¥—ã –¥–ª—è –Ω–∞–≤–∏–≥–∞—Ü–∏–∏.\n")

    while current_step < total_steps:
        # –ü–æ–∫–∞–∑—ã–≤–∞–µ–º —Ç–µ–∫—É—â–∏–π —à–∞–≥
        print(f"–®–∞–≥ {current_step + 1}/{total_steps}:")
        print(f"‚û°Ô∏è  {instructions[current_step]}")

        # –ó–∞–ø—Ä–∞—à–∏–≤–∞–µ–º –¥–µ–π—Å—Ç–≤–∏–µ —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        if current_step == 0:
            # –ü–µ—Ä–≤—ã–π —à–∞–≥
            command = input("\n–í–≤–µ–¥–∏—Ç–µ 'n' (next) –¥–ª—è —Å–ª–µ–¥—É—é—â–µ–≥–æ —à–∞–≥–∞ –∏–ª–∏ 'q' (quit) –¥–ª—è –≤—ã—Ö–æ–¥–∞: ").lower()
        elif current_step == total_steps - 1:
            # –ü–æ—Å–ª–µ–¥–Ω–∏–π —à–∞–≥
            command = input("\n–í–≤–µ–¥–∏—Ç–µ 'p' (prev) –¥–ª—è –ø—Ä–µ–¥—ã–¥—É—â–µ–≥–æ —à–∞–≥–∞, 'r' (restart) –¥–ª—è –Ω–∞—á–∞–ª–∞ –∏–ª–∏ 'q' (quit): ").lower()
        else:
            # –õ—é–±–æ–π –¥—Ä—É–≥–æ–π —à–∞–≥
            command = input("\n–í–≤–µ–¥–∏—Ç–µ 'n' (next), 'p' (prev), 'r' (restart) –∏–ª–∏ 'q' (quit): ").lower()

        # –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ–º –∫–æ–º–∞–Ω–¥—É
        if command in ['n', 'next', '']:
            current_step += 1
        elif command in ['p', 'prev'] and current_step > 0:
            current_step -= 1
        elif command in ['r', 'restart']:
            current_step = 0
            print("\n" + "="*40)
            print("–ù–∞—á–∏–Ω–∞–µ–º –∑–∞–Ω–æ–≤–æ!")
            print("="*40 + "\n")
        elif command in ['q', 'quit']:
            print("\n–í—ã—Ö–æ–¥ –∏–∑ —Ä–µ—Ü–µ–ø—Ç–∞.")
            return
        else:
            print("–ù–µ–∏–∑–≤–µ—Å—Ç–Ω–∞—è –∫–æ–º–∞–Ω–¥–∞. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ —Å–Ω–æ–≤–∞.")

        print()  # –ü—É—Å—Ç–∞—è —Å—Ç—Ä–æ–∫–∞ –¥–ª—è —á–∏—Ç–∞–µ–º–æ—Å—Ç–∏

    # –ï—Å–ª–∏ –¥–æ—à–ª–∏ –¥–æ –∫–æ–Ω—Ü–∞
    print("üéâ –ü–æ–∑–¥—Ä–∞–≤–ª—è–µ–º! –í—ã –∑–∞–≤–µ—Ä—à–∏–ª–∏ —Ä–µ—Ü–µ–ø—Ç!")

# –ó–∞–ø—É—Å–∫–∞–µ–º –∏–Ω—Ç–µ—Ä–∞–∫—Ç–∏–≤–Ω—É—é –≤–µ—Ä—Å–∏—é
recipe_instructions = [
    "–ü–æ–¥–≥–æ—Ç–æ–≤–∏—Ç—å –≤—Å–µ –∏–Ω–≥—Ä–µ–¥–∏–µ–Ω—Ç—ã.",
    "–ù–∞—Ä–µ–∑–∞—Ç—å –æ–≤–æ—â–∏ –∫—É–±–∏–∫–∞–º–∏.",
    "–û–±–∂–∞—Ä–∏—Ç—å –ª—É–∫ –¥–æ –∑–æ–ª–æ—Ç–∏—Å—Ç–æ–≥–æ —Ü–≤–µ—Ç–∞.",
    "–î–æ–±–∞–≤–∏—Ç—å –º–æ—Ä–∫–æ–≤—å –∏ —Ç—É—à–∏—Ç—å 5 –º–∏–Ω—É—Ç.",
    "–ó–∞–ª–∏—Ç—å –≤–æ–¥–æ–π –∏ –¥–æ–≤–µ—Å—Ç–∏ –¥–æ –∫–∏–ø–µ–Ω–∏—è.",
    "–ü–æ—Å–æ–ª–∏—Ç—å –∏ –ø–æ–ø–µ—Ä—á–∏—Ç—å –ø–æ –≤–∫—É—Å—É.",
    "–ü–æ–¥–∞–≤–∞—Ç—å –≥–æ—Ä—è—á–∏–º."
]

run_recipe_interactive(recipe_instructions)